    private void bt_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_AddActionPerformed
        // TODO add your handling code here:

        if (!TF_name.getText().isEmpty() && !TF_id.getText().isEmpty()) {

            try {
                String name = TF_name.getText();
                int id = Integer.parseInt(TF_id.getText());

                String gender;

                if (RbtMale.isSelected()) {
                    gender = RbtMale.getText().toString();
                } else {
                    gender = RbtFemale.getText().toString();
                }

                String department = CBdepatrment.getSelectedItem().toString();

                PreparedStatement stm = con.prepareStatement("insert into students (student_name , student_id , student_gender, student_department) \n"
                        + "values( ? , ? , ? , ? )");

                stm.setString(1, name);
                stm.setInt(2, id);
                stm.setString(3, gender);
                stm.setInt(4, cbdepartmentindex.get(CBdepatrment.getSelectedIndex()));

                stm.executeUpdate();
                JOptionPane.showMessageDialog(this, "added sucssufly");
                dtm.addRow(new Object[]{name, id, gender, department});
                Tbshow.setModel(dtm);

            } catch (SQLException ex) {
                Logger.getLogger(Rform.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_bt_AddActionPerformed

