    private void fillcombobox() {
        try {
            PreparedStatement stm = con.prepareStatement("select depart_name , depart_id from departments ");
            ResultSet rs = stm.executeQuery();

            while (rs.next()) {

                CBdepatrment.addItem(rs.getString(1));
                cbdepartmentindex.add(rs.getInt(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Rform.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void fillTable() {

        dtm.setRowCount(0);
        try {
            PreparedStatement stm = con.prepareStatement("select student_name , student_id , student_gender , depart_name from students , departments where students.student_department = departments.depart_id ");
            ResultSet rs = stm.executeQuery();

            while (rs.next()) {

                dtm.addRow(new Object[]{rs.getString(1), rs.getInt(2), rs.getString(3), rs.getString(4)});
                studentindex.add(rs.getInt(2));

            }
            Tbshow.setModel(dtm);

        } catch (SQLException ex) {
            Logger.getLogger(Rform.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

